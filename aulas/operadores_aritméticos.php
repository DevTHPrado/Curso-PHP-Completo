<?php 
    //OPERADORES ARITMÉTICOS
    $r = 2 + 2;
    echo "$r"; // 4

    $s = "2" + "2";
    var_dump("$s"); // 4

    // Para o PHP, mesmo se o 2 estiver com as aspas, se utilizar o sinal de + ele vai dar 4, pois ele idendifica que o usuário ta tentando fazer uma adição de números inteiros, para fazer concatenação utilizamos o ".", ai sim ele n ira só e ira dar "22"

    $c = "2" . "2";
    echo "\n$c"; // "22"

    /* OPERADORES ARITMÉTICOS
    +: adição
    -: subtração
    *: multiplicação
    /: divisão real
    %: módulo / resto da divisão
    **: exponenciação / potência
    */
?>